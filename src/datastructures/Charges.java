package datastructures;

public class Charges {
	public double cottagecharge;
	public double bungalowcharge;
	public double mansioncharge;
	public double watercharge;
	public double wallcharge;
	
	public Charges(double cottagecharge,double bungalowcharge,double mansioncharge,
			double watercharge,double wallcharge)
	{
		this.cottagecharge=cottagecharge;
		this.bungalowcharge=bungalowcharge;
		this.mansioncharge=mansioncharge;
		this.watercharge=watercharge;
		this.wallcharge=wallcharge;
	}
}
